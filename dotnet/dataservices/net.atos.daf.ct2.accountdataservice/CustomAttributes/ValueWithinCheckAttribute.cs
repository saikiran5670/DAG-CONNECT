        public ValueWithinCheckAttribute(string[] values = null)
        : base("Invalid {0}")
        {
            _values = values;
        }

        protected override ValidationResult IsValid(object value, ValidationContext validationContext)
        {
            if (_values == null && validationContext.MemberName.Equals("Language"))
            {
                _values = _languages;
            }
            else if (_values == null && validationContext.MemberName.Equals("TimeZone"))
            {
                _values = _timeZones;
            }

            if (_values.Contains(((string)value).ToLower()))
            {
                return ValidationResult.Success;
            }
            else
            {
                return new ValidationResult(ErrorMessageString);
            }
        }

